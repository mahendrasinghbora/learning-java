class test10 {
    public static void main(String[] args) {
        int int_max = 0X7fffffff;         //Maximum value that an integer variable can hold.
        System.out.println("MAXIMUM VALUE OF AN \'int\': " + int_max);
        short short_max = 0X7fff;         //Maximum value that a short variable can hold.
        System.out.println("MAXIMUM VALUE OF A \'short\': " + short_max);
        byte byte_max = 0X7f;         //Maximum value that a byte variable can hold.
        System.out.println("MAXIMUM VALUE OF A \'byte\': " + byte_max);
        long long_max = 0X7fffffffffffffffL;          //Maximum value that a long variable can hold.
        System.out.println("MAXIMUM VALUE OF A \'long\': " + long_max);
    }
}
